				<h3>What can Cloud Learning do?</h3>
				<ul>
					<li><b>Allow access to learning resources anytime, anywhere</b> - If you make resources available on the cloud, your students will have access to them in lessons but also at home or anywhere else where there is an internet connection. </li>
					<li><b>Foster independent learning</b> - Cloud Learning can be used to encourage pupils to learn independently and go beyond the curriculum.</li>
					<li><b>Provide both extension and support</b> - As the resources available on the cloud build up, students will have access to an ever growing set of resources that is always only a click or a tap away. As you create new resources you could easily differentiate by linking to resources to extend the more able, as well as other links to support those who need it. </li>
					<li><b>Showcase students’ work</b> - Either the work students complete for specific subjects or perhaps a space dedicated exclusively to this purpose, such as the Walbottle Press website.</li>
					<li><b>Be a peer assessment tool</b> - Cloud Learning allows comments (they can also be turned off). This means that we can exploit the social nature of the web to encourage our students to peer-assess, leaving a permanent record of such assessment, not just for each other, but also for the teacher, who can then plan future lessons informed by the comments students left for their peers. This can also be used for more formative assessment and feedback/feedforward from teachers.</li>
					<li><b>Bridge the gap between formal and informal learning</b> - One of the most interesting aspects of the cloud is that it can be accessed anytime, anywhere. Combine this with the fact that we are not limited to curriculum content and we can begin to imagine ways in which we use the cloud to pique our students’ curiosity about our subjects.</li>
					<li><b>Develop a greater understanding of digital citizenship</b> - Our students have grown up associating the internet with leisure. However, as they begin to use online resources for more academic purposes – through our cloud – we hope that a more mature attitude to the use of online resources will emerge. By encouraging our students to publish their own content, we are supporting the development of their digital literacy and citizenship.</li>
					<li><b>Disassociate learning from a particular time at a particular place</b> - Whilst we recognise that the classroom is still the primary place for learning, we acknowledge that ubiquitous access to knowledge and information means that the classroom is not the only place for learning. Using the cloud we can deliver learning to wherever the learner happens to be.</li>
					<li><b>Encourage collaborative practices</b> - We have referred above to the social nature of the internet. It is worth pointing out that the cloud can be harnessed to foster collaboration both online and offline.</li>
					<li><b>Provide students with digital portfolios</b> - Something it is rather easy to forget is the fact that the cloud can provide students as well as teachers with a digital space to call their own.</li>
					<li><b>Raise the profile of subjects and departments</b> - As any Head of Department knows well, one of the most challenging and important things to do is to raise the profile of your subject or department, especially if it’s an option subject from early on. The cloud can play an important role in this.</li>
					<li><b>Be an outlet for news, announcements and developments</b> - The cloud can be used very effectively to keep students, staff, parents and members of the wider school community updated.</li>
					<li><b>Gather and record evidence of learning and progress over a period of time</b> - As new entries are added to the cloud showcasing pupils’ work, providing opportunities for peer-assessment, or simply just gradually contributing to the subject content, a growing and compelling record of learning and progress will begin to emerge.</li>
					
				</ul>
